from math import sin, cos, tan, radians
a=int(input('Digite o valor do 창ngulo em graus:'))
rad=radians(a)
print('O valor do seno do 창ngulo {} vale: {}'.format(a,sin(rad)))
print('O valor do cosseno do 창ngulo {} vale: {}'.format(a,cos(rad)))
print('O valor da tangente do 창ngulo {} vale: {}'.format(a,tan(rad)))
